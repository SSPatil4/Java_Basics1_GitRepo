package encapPackage1;

public class AreaRectangle {

    private int l, b;

    public int getL() {
        return l;
    }

    public int getB(){
        return b;
    }

    public void setL(int l){
        this.l=l;
    }

    public void setB(int b){
        this.b=b;
    }

    public int getArea(){

        int area =l*b;
        return area;
    }






}
